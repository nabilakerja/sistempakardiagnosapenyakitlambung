    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
    // TODO add your handling code here:   
    String id = tid.getText();
    String namaPasien = tnama.getText();
    String jenisKelamin = rjk1.isSelected() ? "Laki-Laki" : "Perempuan";
    String usia = tusia.getText();
    String noHp = tnohp.getText();
    String alamat = talm.getText();

    if (id.isEmpty() || namaPasien.isEmpty() || usia.isEmpty() || noHp.isEmpty() || alamat.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } else {
        try {
            String sql = "INSERT INTO pasien (pasien_id, nama_pasien, jenis_kelamin, usia, no_hp, alamat) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement stat = conn.prepareStatement(sql);
            
            
            stat.setString(1, id);
            stat.setString(2, namaPasien);
            stat.setString(3, jenisKelamin);
            stat.setInt(4, Integer.parseInt(usia));
            stat.setString(5, noHp);
            stat.setString(6, alamat);

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            kosong(); // Metode untuk mengosongkan semua field setelah menyimpan data
            autoIDPasien(); // Update the auto-generated ID, if necessary
            datatable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnsaveActionPerformed

