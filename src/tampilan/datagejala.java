    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Object[] Baris = {"ID Gejala", "Nama Gejala"};
        tabmode = new DefaultTableModel(null, Baris);
        tablegejala.setModel(tabmode);

        // Validasi input pencarian
        if (tcari.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan nama gejala untuk mencari!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Query untuk mencari data pasien berdasarkan huruf depan nama
        String sql = "SELECT * FROM gejala WHERE nama_gejala LIKE ?";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tcari.getText().trim() + "%"); // Menambahkan wildcard untuk mencocokkan huruf depan
            ResultSet hasil = stat.executeQuery();

            // Ambil data dari ResultSet
            while (hasil.next()) {
                String a = hasil.getString("id_gejala");
                String b = hasil.getString("nama_gejala");
                String[] data = {a, b};
                tabmode.addRow(data);
            }

            // Jika tidak ada data ditemukan
            if (tabmode.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Data gejala tidak ditemukan!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

