    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
    // TODO add your handling code here:
    Object[] Baris = {"ID Pasien", "Nama Pasien", "Jenis Kelamin", "Usia", "No.Handphone", "Alamat"};
    tabmode = new DefaultTableModel(null, Baris);
    tablepasien.setModel(tabmode);

    // Validasi input pencarian
    if (tcari.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Masukkan nama pasien untuk mencari!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Query untuk mencari data pasien berdasarkan huruf depan nama
    String sql = "SELECT * FROM pasien WHERE nama_pasien LIKE ?";
    try {
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, tcari.getText().trim() + "%"); // Menambahkan wildcard untuk mencocokkan huruf depan
        ResultSet hasil = stat.executeQuery();

        // Ambil data dari ResultSet
        while (hasil.next()) {
            String a = hasil.getString("pasien_id");
            String b = hasil.getString("nama_pasien");
            String c = hasil.getString("jenis_kelamin");
            String d = hasil.getString("usia");
            String e = hasil.getString("no_hp");
            String f = hasil.getString("alamat");
            String[] data = {a, b, c, d, e, f};
            tabmode.addRow(data);
        }

        // Jika tidak ada data ditemukan
        if (tabmode.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Data pasien tidak ditemukan!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }


    }//GEN-LAST:event_btncariActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        try {
            String sql = "update pasien set nama_pasien=?, jenis_kelamin=?, usia=?, no_hp=?, alamat=? where pasien_id=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tnama.getText());

            String jkel = "";
            if (rjk1.isSelected()) {
                jkel = "Laki-laki";
            } else {
                jkel = "Perempuan";
            }
            stat.setString(2, jkel);

            stat.setString(3, tusia.getText());
            stat.setString(4, tnohp.getText());
            stat.setString(5, talm.getText());
            stat.setString(6, tid.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
            kosong(); // Possibly resets input fields
            tid.requestFocus(); // Sets focus back to a particular input field
            datatable(); // Refreshes the displayed data table

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah " + e);
        }
    }//GEN-LAST:event_btneditActionPerformed

