    private void autoIDPasien() {
        try {
        // Query untuk mendapatkan ID gejala terakhir
        String sql = "SELECT pasien_id FROM pasien ORDER BY pasien_id DESC LIMIT 1";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        if (rs.next()) {
            // Mengambil ID terakhir dan menambahkan 1 untuk ID baru
            int idTerakhir = rs.getInt("pasien_id");
            int idBaru = idTerakhir + 1;
            tid.setText(String.valueOf(idBaru)); // Menampilkan ID baru pada field
        } else {
            // Jika tabel kosong, mulai dari ID 1
            tid.setText("1");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal membuat ID Pasien: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

