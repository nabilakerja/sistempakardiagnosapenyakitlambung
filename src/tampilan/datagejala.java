    private void autoIDGejala() {
    try {
        // Query untuk mendapatkan ID gejala terakhir
        String sql = "SELECT id_gejala FROM gejala ORDER BY id_gejala DESC LIMIT 1";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        if (rs.next()) {
            // Mengambil ID terakhir dan menambahkan 1 untuk ID baru
            int idTerakhir = rs.getInt("id_gejala");
            int idBaru = idTerakhir + 1;
            tid.setText(String.valueOf(idBaru)); // Menampilkan ID baru pada field
        } else {
            // Jika tabel kosong, mulai dari ID 1
            tid.setText("1");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal membuat ID Gejala: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
    }
}


    protected void datatable() {
    Object[] Baris = {"ID Gejala", "Nama Gejala"};
    tabmode = new DefaultTableModel(null, Baris);
    tablegejala.setModel(tabmode);
    String sql = "select * from gejala";
    try {
        java.sql.Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        while (hasil.next()) {
            String a = hasil.getString("id_gejala");
            String b = hasil.getString("nama_gejala");
            String[] data = {a, b};
            tabmode.addRow(data);
        }
        // Atur ukuran panjang per kolom
        tablegejala.getColumnModel().getColumn(0).setPreferredWidth(250);  // ID Aturan
        tablegejala.getColumnModel().getColumn(1).setPreferredWidth(1000); // Nama Gejala
    } catch (Exception e) {
        e.printStackTrace();
    }
    }

